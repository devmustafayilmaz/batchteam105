package ders13_NestedForLoop;

public class C01_NestedForLoop {
    public static void main(String[] args) {
       // 1-4 arasi çarpım tablosu yazdirin

        for (int i = 1; i <=4; i++) {
            for (int j = 1; j <= 4; j++) {
                System.out.print(i*j + " ");
            }
            System.out.println("");
        }




    }
}
